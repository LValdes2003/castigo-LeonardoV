public class Main {
    public static void main(String[] args) {
        // Mayor número entero representable por un char
        char mayorChar = Character.MAX_VALUE;
        System.out.println("Mayor número entero representable por un char: " + (int) mayorChar);

        // Mayor número entero representable por un short
        short mayorShort = Short.MAX_VALUE;
        System.out.println("Mayor número entero representable por un short: " + mayorShort);

        // Mayor número entero representable por un int
        int mayorInt = Integer.MAX_VALUE;
        System.out.println("Mayor número entero representable por un int: " + mayorInt);
    }
}
